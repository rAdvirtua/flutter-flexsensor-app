// Function to fetch ndkVersion dynamically from local.properties
fun ndkVersionFromLocalProperties(): String {
    val localProperties = File(rootProject.projectDir, "local.properties")
    if (localProperties.exists()) {
        val properties = Properties()
        properties.load(FileInputStream(localProperties))
        return properties.getProperty("ndkVersion", "26.3.11579264") // Default fallback
    }
    return "26.3.11579264" // Default if no property is found
}
